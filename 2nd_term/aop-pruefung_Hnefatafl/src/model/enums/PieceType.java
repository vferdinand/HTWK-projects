package model.enums;

/**
 * Enum, das die verschiedenen Spielsteine repräsentiert.
 * 
 * Diese Enum definiert die möglichen Typen von Steinen. Jeder Zelltyp hat eine
 * spezifische Rolle im Kontext des Spiels:
 * 
 * <pre>
 * - ATTACKER: Repräsentiert einen Angreifer.
 * - DEFENDER: Repräsentiert einen Verteidiger.
 * - KING: Repräsentiert den König.
 * </pre>
 */
public enum PieceType {
	ATTACKER, DEFENDER, KING;
}
